제8조 쿠키()의 운영 및 활용<br/>
<br/>
<p>
1. 사이트는 개인화된 서비스를 제공하기 위하여 ‘쿠키’를 사용하기도 합니다. ‘쿠키’는 HTTP 서버에서 사용자의 브라우저에서 보내는 소량의 데이터로서 이용자의 컴퓨터 하드드라이브에 저장됩니다. 쿠키는 이용자의 컴퓨터는 식별하지만 이용자를 개인적으로 식별하지는 않습니다.
<br/>2. 쿠키의 사용 목적
가. 회원과 비회원의 식별 및 회원에게 필요한 정보 제공.
<br/>나. 쿠키 브라우저의 종료시나 로그아웃 시 만료됩니다. 쿠키는 하루가 지나면 만료되며, 브라우저의 ‘쿠키삭제’시 삭제할 수 있습니다.
<br/>* 쿠키의 설치 및 거부
<br/>- 이용자는 쿠키 설치에 대한 선택권을 가지고 있습니다. 따라서 귀하는 웹브라우저에서 옵션을 설정함으로써 모든 쿠키를 허용하거나, 쿠키가 저장될 때마다 확인을 거치거나, 모든 쿠키의 저장을 거부할 수 있습니다.
<br/>- 다만, 쿠키의 저장을 거부할 경우에는 로그인이 필요한 일부 서비스 이용에 어려움이 있을 수 있습니다.
<br/>- 쿠키 설치 허용 여부를 지정하는 방법 (Internet Explorer의 경우)
<br/>(1) [도구] 메뉴에서 [인터넷 옵션]을 선택
<br/>(2) [개인정보]를 클릭
<br/>(3) [고급]을 클릭
<br/>(4) 쿠키 허용여부를 선택
<br/>* 쿠키 설치 허용 여부를 지정하는 방법 (Safari의 경우)
<br/>(1) MAcOS 상단 좌측 메뉴바에서 [Safari][환경설정]을 선택
<br/>(2) [환경설정] 창에서 [보안]으로 이동하여 쿠키 허용여부 선택
<br/>다. 사이트가 진행하는 각종 이벤트에서 이용자의 참여 정도 및 방문 수를 파악하여 차별적인 응모 기회를 부여하고 개인의 관심분야에 따라 차별화된 정보를 제공하기 위한 자료로 이용됩니다.
<br/>라. 이용자는 쿠키 설치에 대한 선택권을 가지고 있습니다. 따라서 이용자는 웹 브라우저에서 옵션을 설정함으로써 모든 쿠키를 허용하거나, 쿠키가 저장될 때마다 확인을 거치거나, 아니면 오든 쿠키의 저장을 거부 할 수도 있습니다.
<br/>마. 이용자의 선택에 의해 쿠키의 저장을 거부하실 수 있으나, 이 경우 정상적인 웹서비스를 이용하지 못할 수도 있습니다.
</p>


